CREATE EXTENSION IF NOT EXISTS pgcrypto;


-- ─────────────────────────────────────────────────────────────────────────────
-- SECCIÓN 3: Tipos enumerados (ENUMs)
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TYPE tipo_sexo AS ENUM ('M','F');
CREATE TYPE tipo_dosis AS ENUM ('UNICA','SERIE_PRIMARIA','REFUERZO','ANUAL','ADICIONAL');
CREATE TYPE tipo_alerta_dosis AS ENUM ('APLICABLE','ATRASADA','CERCA_LIMITE','FALTANTE');
CREATE TYPE tipo_alerta_inv AS ENUM ('CERCA_AGOTAR','CERCA_CADUCAR','AGOTADO','CADUCADO');


