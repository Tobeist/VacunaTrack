    IN  p_prim_nombre  VARCHAR(100), IN  p_seg_nombre   VARCHAR(100),
    IN  p_apellido_pat VARCHAR(100), IN  p_apellido_mat VARCHAR(100),
    IN  p_telefono     VARCHAR(20),  IN  p_curp         VARCHAR(18),
