CREATE OR REPLACE PROCEDURE sp_transferir_inventario(
    IN    p_inv_origen_id     INTEGER,
    IN    p_centro_destino_id INTEGER,
    IN    p_cantidad          INTEGER,
    INOUT p_ok  SMALLINT,
    INOUT p_msg VARCHAR(200),
    INOUT p_id  INTEGER
)
LANGUAGE plpgsql AS $$
DECLARE
    v_centro_origen_id INTEGER;
    v_lote_id          INTEGER;
    v_stock_actual     INTEGER;
    v_nuevo_inv_id     INTEGER;
BEGIN
    IF p_cantidad <= 0 THEN
        p_ok := 0; p_msg := 'La cantidad debe ser mayor a cero.'; RETURN;
    END IF;

    SELECT centro_id, lote_id, inventario_stock_actual
    INTO   v_centro_origen_id, v_lote_id, v_stock_actual
    FROM   inventarios
    WHERE  inventario_id = p_inv_origen_id
      AND  inventario_activo_desde IS NOT NULL;

    IF NOT FOUND THEN
        p_ok := 0; p_msg := 'El inventario de origen no existe o no está activo.'; RETURN;
    END IF;

    IF p_centro_destino_id = v_centro_origen_id THEN
        p_ok := 0; p_msg := 'El centro de destino debe ser diferente al de origen.'; RETURN;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM centros_salud WHERE centro_id = p_centro_destino_id) THEN
        p_ok := 0; p_msg := 'El centro de destino no existe.'; RETURN;
    END IF;

    IF p_cantidad > v_stock_actual THEN
        p_ok := 0;
        p_msg := 'La cantidad a transferir (' || p_cantidad || ') supera el stock disponible (' || v_stock_actual || ').';
        RETURN;
    END IF;

    UPDATE inventarios
    SET    inventario_stock_actual = inventario_stock_actual - p_cantidad
    WHERE  inventario_id = p_inv_origen_id;

    INSERT INTO inventarios(
        centro_id, lote_id,
        inventario_stock_inicial, inventario_stock_actual,
        inventario_activo_desde, usuario_id, inventario_origen_id
    ) VALUES (
        p_centro_destino_id, v_lote_id,
        p_cantidad, p_cantidad,
        NULL, NULL, p_inv_origen_id
    ) RETURNING inventario_id INTO v_nuevo_inv_id;

    INSERT INTO transferencias_inventario(inv_origen_id, inv_destino_id)
    VALUES (p_inv_origen_id, v_nuevo_inv_id);

    p_ok  := 1;
    p_msg := 'Transferencia registrada. El responsable del centro destino debe confirmar la recepción.';
    p_id  := v_nuevo_inv_id;
END; $$;

-- ─────────────────────────────────────────────────────────────────────────────
-- Esquemas versionados: conflictos y asignación semi-automática
-- ─────────────────────────────────────────────────────────────────────────────

CREATE OR REPLACE VIEW vw_conflictos_esquema AS
WITH esquema_activo AS (
    SELECT esquema_id, esquema_nombre
    FROM   esquemas
    WHERE  esquema_vigente_hasta IS NULL
    ORDER  BY esquema_vigente_desde DESC
    LIMIT  1
)
SELECT
    p.paciente_id,
    p.paciente_prim_nombre,
    p.paciente_seg_nombre,
    p.paciente_apellido_pat,
    p.paciente_apellido_mat,
    p.paciente_fecha_nac,
    (CURRENT_DATE - p.paciente_fecha_nac)   AS edad_dias,
    p.esquema_id                             AS esquema_actual_id,
    e_viejo.esquema_nombre                   AS esquema_actual_nombre,
    ea.esquema_id                            AS esquema_nuevo_id,
    ea.esquema_nombre                        AS esquema_nuevo_nombre,
    d.dosis_id                               AS dosis_conflicto_id,
    d.dosis_tipo,
    d.dosis_edad_oportuna_dias,
    d.dosis_limite_edad_dias,
    v.vacuna_nombre
FROM   pacientes      p
JOIN   esquemas       e_viejo ON e_viejo.esquema_id = p.esquema_id
                              AND e_viejo.esquema_vigente_hasta IS NOT NULL
CROSS  JOIN esquema_activo    ea
JOIN   dosis_esquemas de_v    ON de_v.esquema_id    = p.esquema_id
JOIN   dosis          d       ON d.dosis_id         = de_v.dosis_id
JOIN   vacunas        v       ON v.vacuna_id        = d.vacuna_id
WHERE
    NOT EXISTS (
        SELECT 1 FROM dosis_esquemas de_n
        WHERE  de_n.esquema_id = ea.esquema_id
          AND  de_n.dosis_id   = d.dosis_id
    )
    AND NOT EXISTS (
        SELECT 1 FROM aplicaciones a
        WHERE  a.paciente_id = p.paciente_id
          AND  a.dosis_id    = d.dosis_id
    )
    AND (d.dosis_limite_edad_dias IS NULL
         OR (CURRENT_DATE - p.paciente_fecha_nac) < d.dosis_limite_edad_dias)
    AND NOT EXISTS (
        SELECT 1 FROM esquemas_pacientes ep
        WHERE  ep.paciente_id           = p.paciente_id
          AND  ep.esquema_id            = p.esquema_id
          AND  ep.esq_pac_motivo_cambio LIKE 'conflicto_resuelto:%'
    );

