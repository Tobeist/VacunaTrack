CREATE OR REPLACE VIEW vw_transferencias AS
SELECT
    t.transf_id,
    t.transf_timestamp,
    t.inv_origen_id,
    t.inv_destino_id,
    cso.centro_nombre                    AS origen_centro_nombre,
    csd.centro_nombre                    AS destino_centro_nombre,
    lo.lote_codigo,
