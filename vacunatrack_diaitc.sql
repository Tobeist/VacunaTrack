    OUT p_ok SMALLINT, OUT p_msg VARCHAR(150), OUT p_id INTEGER
)
LANGUAGE plpgsql AS $$
DECLARE v_ok SMALLINT; v_msg VARCHAR(150); v_id INTEGER;
BEGIN
